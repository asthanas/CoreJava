package co.edureka.lang;

public class MathAPI {

	public static void main(String[] args) {
		
		System.out.println(">> PI is: "+Math.PI);
		System.out.println(">> E is: "+Math.E);
		
		System.out.println(">> Square Root of 16 is: "+Math.sqrt(16));
		System.out.println(">> Log 100 to the base 10  is: "+Math.log10(100));
		System.out.println(">> ceil of 5.7  is: "+Math.ceil(5.7));
		System.out.println(">> floor of 5.4  is: "+Math.floor(5.4));
		

	}

}
